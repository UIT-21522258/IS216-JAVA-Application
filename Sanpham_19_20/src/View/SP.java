    private void ThemBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThemBtnActionPerformed
        // TODO add your handling code here:
        if(Matxt.getText().equals("") || TenSPtxt.getText().equals("") || DG.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui long nhap day du thong tin");
            return;
        } else {
            Connection con = null;
            try {
                con = ConnectDB.getConn();
            } catch(Exception e) {
                e.printStackTrace();
            }
            String sql = "INSERT INTO SANPHAM (TENSP, SOLUONG, DONGIA, MANCC) VALUES (?, ?, ?, ?)";
            int sl = 0;
            try {
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, TenSPtxt.getText());
                ps.setInt(2, sl);
                ps.setLong(3, Long.parseLong(DG.getText()));
                ps.setString(4, Matxt.getText());
                int oke = ps.executeUpdate();
                if(oke > 0)
                    JOptionPane.showMessageDialog(this, "Thanh cong");
                else 
                    JOptionPane.showMessageDialog(this, "That bai");
            } catch(Exception e) {
                e.printStackTrace();
            }
        }
       
    }//GEN-LAST:event_ThemBtnActionPerformed

    private void MatxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_MatxtKeyPressed
        // TODO add your handling code here:
        TenNcctxt.setText("");
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            Connection con = null;
            try {
                con = ConnectDB.getConn();
            }catch (Exception e) {
                e.printStackTrace();
            }
            String sql = "SELECT TENNCC FROM NHACC WHERE MANCC = ?";
            int sl = 0;
            try {
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, Matxt.getText());
                ResultSet rs = ps.executeQuery();
                if(rs.next()){
                    TenNcctxt.setText(rs.getString("TENNCC"));
                    sl += 1;
                }   
                
                if(sl == 0) {
                    JOptionPane.showMessageDialog(this, "Khong tim thay");
                    return;
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_MatxtKeyPressed

    
    
