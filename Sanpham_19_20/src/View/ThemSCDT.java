    public void showCV(){
        Connection con = null;
        try {
            con = ConnectDB.getConn();
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        String sql = "SELECT MACV, TENCV FROM CONGVIEC";
        
        tb1.setColumnIdentifiers(new String[]{"Ma cong viec", "Ten cong viec"});
        tb1.setRowCount(0);
        try {
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                tb1.addRow(new String[] {rs.getString("MACV"), rs.getString("TENCV")});
            }
            CV.setModel(tb1);
        } catch(Exception e) {
            e.printStackTrace();
        }
        setVisible(true);
    }
    
