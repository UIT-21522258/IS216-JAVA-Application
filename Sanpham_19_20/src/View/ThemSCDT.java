    private void CVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CVMouseClicked
        // TODO add your handling code here:
        long dg = 0;
        int selectedRow = CV.getSelectedRow();
        tb2 = (DefaultTableModel) DSCV.getModel();
        if(selectedRow >= 0 && selectedRow <= tb1.getRowCount()){
            Object[] row = new Object[2];
            Connection con = null;
            try {
                con = ConnectDB.getConn();
            } catch(Exception e) {
                e.printStackTrace();
            }
            String sql = "SELECT DONGIA FROM CONGVIEC WHERE MACV = ?";
            try {
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, tb1.getValueAt(selectedRow, 0).toString());
                ResultSet rs = ps.executeQuery();
                if(rs.next()) {
                    dg = rs.getLong("DONGIA");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            row[0] = tb1.getValueAt(selectedRow, 1);
            row[1] = dg;
            tb2.addRow(row);
        }
    }//GEN-LAST:event_CVMouseClicked

    private void ThemBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThemBtnActionPerformed
        // TODO add your handling code here:
        if(Imeitxt.getText().equals("") || NgL.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui long nhap day du thong tin");
            return;
        } else {
            Connection con = null;
            try {
                con = ConnectDB.getConn();
            } catch(Exception e) {
                e.printStackTrace();
            }
            long dg = 0, sum = 0;
            String tencv = null;
            int sl = tb2.getRowCount();
            String MPSC = getMaPSC(Imeitxt.getText(), NgL.getText());
            for(int i=0; i<sl; i++){
                String sql = "INSERT INTO CT_SC(MAPSC, MACV) VALUES (?, ?)";
                try {
                    PreparedStatement ps = con.prepareStatement(sql);
                    ps.setString(1, MPSC);
                    tencv = tb2.getValueAt(i, 0).toString();
                    ps.setString(2, getMaCV(tencv));
                    int oke = ps.executeUpdate();
                    dg = Long.parseLong(tb2.getValueAt(i, 1).toString());
                    sum += dg;
                    if(oke == 0){
                        JOptionPane.showMessageDialog(this, "Failed");
                        sum -= dg;
                        return;
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
            
            if(sum > 0){
                String sql = "UPDATE SUACHUA SET TONGTIEN = ? WHERE MAPSC = ?";
                try {
                    PreparedStatement ps = con.prepareStatement(sql);
                    ps.setLong(1, sum);
                    ps.setString(2, MPSC);
                    int oke = ps.executeUpdate();
                    if(oke  > 0){
                        JOptionPane.showMessageDialog(this, "Success");
                        Imeitxt.setText("");
                        HoTen.setText("");
                        Tentxt.setText("");
                        NgL.setText("");
                        ND.setText("");
                        ShowDSCV();
                    } else {
                        JOptionPane.showMessageDialog(this, "Failed");
                        return;
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Failed");
                return;
            }
        }
    }//GEN-LAST:event_ThemBtnActionPerformed

